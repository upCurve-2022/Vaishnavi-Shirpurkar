package Java_Challenges;

public class StringConcatenation {
    public static void main(String args[]){
        String str1 = "Vaishnavi", str2 = "Shirpurkar";
        System.out.println("String 1: " + str1
                + "\nString 2: " + str2
                +"\nString Concatenation: " + (str1 + str2));
    }
}