public class SimpleInterest {
    public static void main(String args[]){
        // Principal Amount, Rate, Time, Simple Interest
        float p, r, t, si;
        p = 15000;  r = 12; t = 2;
        si = (p*r*t)/100;
        System.out.println("Simple Interest is "+si);
    }
}
